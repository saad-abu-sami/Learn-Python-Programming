'''Python offers modules like math and random tocarry out different mathematics
 like trigonometry,logarithms, probability and statistics, etc.'''
import math
print(math.pi)
print(math.cos(math.pi))
print(math.exp(10)) #e^10 on calculator
print(math.log10(1000)) #long10^10^3 means-3 only
print(math.sinh(1))
print(math.factorial(6)) #factorial 6*5*4*3*2*1=720
print(math.sqrt(100)) #squareroot of 100 is 10
print(math.pow(2,3)) #power 2^3=8
values = [0.99999,1,2,3]
print(math.fsum(values)) #to add many numbers used- fsum
n = 100.7
print(math.floor(n)) #Changing the value of the previous number without taking the number after the decimal
print(math.ceil(n)) #Change the value of the previous number by holding the number after the decimal
print(math.trunc((345.67))) #Truncate Used to delete the number after the decimal